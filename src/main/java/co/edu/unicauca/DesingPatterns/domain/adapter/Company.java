package co.edu.unicauca.DesingPatterns.domain.adapter;

/**
 *
 * @author User
 */
public class Company {

    private String name;

    public Company() {
    }

    public Company(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Company{name='" + name + "'}";
    }
}

